#include <stdio.h>

/**
 * add - Addition of two integers
 * @a: First int
 * @b: Second int
 *
 * Return: Result of the added integers
 */
int add(int a, int b)
{
	return(a + b);
}

/**
 * sub - Subtraction of two integers
 * @a: First int
 * @b: Second int
 *
 * Return: Result of subtracted integers
 */
int sub(int a, int b)
{
	return(a - b);
}

/**
 * mul - Multiplication of two integers
 * @a: First int
 * @b: Second int
 *
 * Return: Result of multiplied integers
 */
int mul( int a, int b)
{
	return(a * b);
}

/**
 * div - Division of two integers
 * @a: First int
 * @b: Second int
 *
 * Return: Result of divided integers
 */
int div(int a, int b)
{
	return(a / b);
}

/**
 * mod - Calculate the remainder of the division of two integers
 * @a: First int
 * @b: Second int
 * 
 * Return: Result of the remainder of the divided integers
 */
int mod(int a, int b)
{
	return(a % b);
}
